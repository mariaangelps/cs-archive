public class SentenciaSwitch {
    public static void main(String[] args){
        String colorModeSelected = "Dark";
        switch(colorModeSelected){
            case"LIGHT":
            System.out.println("Seleccionaste Light Mode");break;
            case "Night:":
                System.out.println("Sleccionaste Night Mode"); break;
            case "Blue Dark":
                System.out.println("Seleccionaste Blue Dark Mode"); break;
            case "Dark":
                System.out.println("Seleccionaste Dark Mode");break;
            default: System.out.println("Seleccionaste naranja");break;


        }
    }
}
